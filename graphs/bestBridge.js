var shortestBridge = function (grid) {
  let graph = {};

  let total = grid.length * grid[0].length;

  for (let i = 0; i < total; i++) {
    let col = i % grid[0].length;
    let row = Math.floor(i / grid[0].length);

    let dirs = [
      [1, 0],
      [-1, 0],
      [0, -1],
      [0, 1],
    ];

    let s = i.toString();

    graph[s] = [];

    for (let dir of dirs) {
      let colDir = col + dir[0];
      let rowDir = row + dir[1];

      let dirS = colDir + rowDir * grid[0].length;

      if (
        colDir >= 0 &&
        colDir < grid[0].length &&
        rowDir >= 0 &&
        rowDir < grid.length
      ) {
        if (s in graph) {
          graph[s].push(dirS.toString());
        }
      }
    }
  }
  let visiteds = new Set();
  let land = [];
  let visited = new Set();

  for (let src in graph) {
    let colSrc = parseInt(src) % grid[0].length;
    let rowSrc = Math.floor(parseInt(src) / grid[0].length);

    if (grid[rowSrc][colSrc] == 0) continue;

    depthFirst(graph, src, visited, grid);

    land = visited;

    break;
  }

  return breathFirst(land, graph, grid);
};

const breathFirst = (lands, graphs, grid) => {
  let shortMin = Number.POSITIVE_INFINITY;

  let visited = new Set();

  let queue = [];

  for (let source of lands) {
    queue.push([source, 0]);
  }

  while (queue.length > 0) {
    let [current, distance] = queue.shift();

    let col = current % grid[0].length;
    let row = Math.floor(current / grid[0].length);

    if (grid[row][col] == 0) {
      distance++;
    }

    if (grid[row][col] == 1 && !lands.has(current)) {
      shortMin = Math.min(shortMin, distance);
      break;
    }

    for (let neighbor of graphs[current]) {
      if (visited.has(neighbor)) continue;
      visited.add(neighbor);
      queue.push([neighbor, distance]);
    }
  }

  return shortMin;
};

const depthFirst = (graphs, src, visited, grid) => {
  let colSrc = parseInt(src) % grid[0].length;
  let rowSrc = Math.floor(parseInt(src) / grid[0].length);

  if (grid[rowSrc][colSrc] == 0) {
    return;
  }

  visited.add(src);

  for (let neighbor of graphs[src]) {
    if (visited.has(neighbor)) continue;
    depthFirst(graphs, neighbor, visited, grid);
  }
};

